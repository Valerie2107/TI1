    <h1>Position géographique de la ville</h1>

        <img src="pages/images/geographie.png" id="geo">
        <p>Le nom officiel complet de la ville est Ville capitale de Varsovie (en polonais : Stołeczne miasto Warszawa)4. Les habitants de Varsovie sont appelés Varsoviens et Varsoviennes.</p>

        <p>La ville est citée à partir du xive siècle sous les noms de Warseuiensis (1321) et Varschewia (1342), puis Warschouia (1482), pour devenir plus tard Warszowa et enfin Warszawa, le nom polonais actuel de Varsovie. Ce nom signifie « appartenant à Warsz », une forme abrégée du prénom masculin Warcisław5. Il s’agit vraisemblablement d’un aristocrate des xiie et xiiie siècles qui possédait un village situé sur l'actuel site du quartier de Mariensztat.</p>

        <p>L'étymologie populaire attribue le nom de la ville à deux personnages légendaires nommés Wars et Sawa : Wars est un pêcheur vivant au bord de la Vistule et Sawa une sirène dont il est tombé amoureux ; c’est cette sirène (en polonais : syrenka) qui est représentée sur les armes de la ville.</p>

        <p>Le 9 novembre 1940, le général Władysław Sikorski ajouta par décret[réf. nécessaire] à ces armes la devise « Semper invicta » (« toujours invincible ») et décerna à la ville la décoration militaire suprême, l'Ordre militaire de Virtuti Militari, en reconnaissance de la bravoure démontrée par ses habitants lors du siège opéré par les forces hitlériennes.</p>

        <p>Varsovie bénéficie d'un climat continental humide de type (Dfb) selon la classification de Köppen.</p>

