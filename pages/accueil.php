            <p>Varsovie est depuis 1596 la capitale de la Pologne et depuis 1999 le chef-lieu de la voïvodie de Mazovie. Elle est située sur la Vistule, à environ 320 km de la mer Baltique et des Carpates. Peuplée par plus d'1,8 million d'habitants (2 millions pour l'agglomération), la capitale polonaise est aussi la plus grande ville du pays et la 8e plus grande de l'Union européenne. Varsovie se divise en dix-huit arrondissements (dzielnice).</p>
            <p>Connue comme la Ville-phénix pour avoir réussi à renaître de ses cendres (84 % de ses bâtiments ont été détruits durant la Seconde Guerre mondiale2), Varsovie a connu une croissance spectaculaire au cours de la seconde moitié du xxe siècle et cette croissance a encore été ravivée après le passage de la Pologne à l’économie de marché dans les années 1990. L’agglomération continue à se transformer et à se développer à un rythme soutenu : elle regroupe toute une gamme d'industries et soixante-six établissements d'enseignement supérieur. C’est aussi un centre artistique et culturel important, une place financière et un pôle économique majeur en Europe centrale.</p>
            <p>Varsovie a donné son nom à la Confédération de Varsovie, au Pacte de Varsovie, au Duché de Varsovie, à la Convention de Varsovie, aux différents Traités de Varsovie et à l'Insurrection de Varsovie. La Varsovienne de 1831 est largement considérée comme l'hymne officieux de Varsovie.</p>
