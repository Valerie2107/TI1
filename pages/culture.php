        <h2>Architecture</h2>
        <p>Les palais, les hôtels particuliers et les églises situés pour la plupart au centre de la ville offrent une richesse de couleurs et de détails issue de presque toutes les périodes et tous les styles architecturaux présents ailleurs en Europe ; on peut y trouver notamment de remarquables exemples des styles gothique, renaissance, baroque, néo-classique et réaliste socialiste.</p>
        <p>L'architecture gothique est représentée par plusieurs édifices du xive siècle et du début du xve siècle : les plus connus sont la cathédrale Saint Jean, de style « gothique mazovien », l’église Notre-Dame, l’hôtel Burbach27), la Tour de la Poudre et la Curia Maior du palais royal.<br>
        L'Architecture de la Renaissance est représentée d’abord par l’hôtel Baryczko (1562) et les édifices appelés « le Nègre » (début du xviie siècle) et « Salwator » (1632), puis laisse place au maniérisme (appelé aussi Renaissance tardive) avec le palais Royal (1596-1619) et l'église des Jésuites (1609-1626).</p>
        <p>Du début de la période baroque on trouve l'église de Sainte-Hyacinthe (1603-1639) et la Colonne de Sigismond (1644), mais c’est surtout au cours des deux dernières décennies du xviie siècle, particulièrement fécondes sur plan architectural, que la ville a vu fleurir les plus beaux témoignages baroques : citons le Palais Krasiński (1677-1683), le Palais de Wilanów (1677-1696) et l'église de Saint-Casimir (1688-1692).<br>
        Le mouvement rococo est également présent avec le Palais Czapski (1712-1721), le Palais des Quatre Vents (1730) et l'église des Visitandines (façade de 1728 à 1761).</p>
        <p>Le courant néo-classique inspire le Palais sur l'eau (1775-1795) (situé dans le parc Łazienki), Królikarnia (1782-1786), l'église des Carmélites (façade de 1761 à 1783) et l'église évangélique de la Sainte-Trinité (1777-1782), puis à la faveur de la croissance économique des premières années du Royaume du Congrès, le renouveau néo-classique bouleverse la ville : les plus remarquables témoignages de cette époque sont le Grand Théâtre (1825-1833) et les bâtiments situés Place de la Banque (1825-1828).</p>
        <p>Des exemples exceptionnels de l'architecture dite « bourgeoise » des périodes suivantes n'ont pas été restaurés par les autorités communistes après la guerre (comme le Palais Kronenberg ou l'immeuble de la compagnie d'assurance «Rosja») ou bien ils ont été reconstruits dans le style du réalisme socialiste (comme le bâtiment de la Philharmonie de Varsovie, initialement inspiré par le Palais Garnier à Paris). Parmi les immeubles du xixe siècle qui ont été préservés, le bâtiment de l'Université technique de Varsovie (1899-1902)28 est sans doute le plus intéressant. Quelques bâtiments ont également été restaurés dans les arrondissements de Praga-Nord et Praga-Sud (sur la rive droite de la Vistule), et la municipalité a décidé de reconstruire le Palais de Brühl et le Palais de Saxe, considérés comme les bâtiments les plus distinctifs de Varsovie d'avant-guerre29.
        Parmi les exemples notables de l'architecture de la seconde moitié du xxe siècle, on peut citer le gigantesque Palais de la culture et de la science (1952-1955) et la Place de la Constitution, avec leur architecture monumentale de style réaliste socialiste30. Comme dans tous les pays ex-communistes, le paysage est dominé par des blocs d'appartements massifs construits entre 1960 et 198531.</p>
        <p>L'architecture moderne à Varsovie est représentée notamment par le Metropolitan Building, construit Place Piłsudski par l’architecte Norman Foster32, la Bibliothèque de l'université de Varsovie (BUW), disposant d'un jardin sur le toit offrant une vue panoramique sur la Vistule (construite par Marek Budzyński et Zbigniew Badowski), le gratte-ciel appelé Rondo 1, bâtiment conçu par Skidmore, Owings and Merrill, les Terrasses d'or, centre commercial composé de sept dômes de verre, etc.</p>

